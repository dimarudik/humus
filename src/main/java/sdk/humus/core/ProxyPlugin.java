package sdk.humus.core;

import java.sql.SQLException;
import java.util.Properties;

/**
 * Основной интерфейс для разработки плагинов расширения драйвера Humus JDBC Proxy.
 * <p>
 * Плагины позволяют перехватывать вызовы JDBC методов (логирование, мониторинг),
 * а также управлять процессом обнаружения (Discovery) узлов базы данных.
 * </p>
 *
 * <p>Жизненный цикл плагина:</p>
 * <ul>
 *     <li>Создается фабрикой {@link ProxyPluginFactory} через Java SPI при установке соединения.</li>
 *     <li>Метод {@link #getTargetUrl} вызывается один раз для определения реального адреса БД.</li>
 *     <li>Метод {@link #execute} вызывается при каждом выполнении SQL-запроса или вызове метода JDBC.</li>
 * </ul>
 */
public interface ProxyPlugin {

    /**
     * Перехватывает выполнение метода JDBC и позволяет добавить произвольную логику
     * до или после вызова целевого драйвера.
     *
     * @param <W>        Тип обертки (ConnectionWrapper, StatementWrapper и т.д.)
     * @param <T>        Тип целевого объекта (реальный объект драйвера PostgreSQL)
     * @param <R>        Тип возвращаемого значения метода
     * @param wrapper    Объект-обертка, вызвавший данный метод.
     * @param target     Реальный объект JDBC драйвера, к которому идет обращение.
     * @param methodName Имя вызываемого метода (например, "execute", "executeQuery").
     * @param next       Функциональный интерфейс для передачи управления следующему плагину в цепочке.
     * @param args       Аргументы, переданные в метод. Могут быть модифицированы плагином.
     * @return Результат выполнения метода.
     * @throws SQLException Если возникла ошибка при выполнении SQL или внутри плагина.
     */
    <W, T, R> R execute(W wrapper, T target, String methodName, JdbcCallable<T, R> next, Object[] args)
            throws SQLException;

    /**
     * Позволяет плагину преобразовать прокси-URL в реальный JDBC URL базы данных.
     * <p>
     * Если плагин реализует логику Discovery (обнаружения), он должен вернуть преобразованный URL.
     * Цепочка вызовов этого метода в драйвере прерывается (break), как только один из плагинов
     * вернет URL, отличный от исходного.
     * </p>
     *
     * @param url  Исходный URL (например, jdbc:humus:grpc://...)
     * @param info Свойства подключения (логин, пароль и др.)
     * @return Преобразованный URL (например, jdbc:postgresql://...) или исходный URL, если плагин его не обрабатывает.
     * @throws SQLException Если процесс обнаружения узла завершился ошибкой.
     */
    default String getTargetUrl(String url, Properties info) throws SQLException {
        return url;
    }

    /**
     * Определяет порядковый номер плагина в цепочке выполнения.
     * <p>
     * Плагины сортируются по возрастанию этого значения. Плагин с наименьшим весом
     * будет находиться "снаружи" (выполнится первым и завершится последним).
     * </p>
     * <ul>
     *     <li>Рекомендуемый диапазон для системных плагинов: 0-500</li>
     *     <li>Рекомендуемый диапазон для пользовательских плагинов: 501-1000</li>
     * </ul>
     *
     * @return Вес (приоритет) плагина. По умолчанию 0.
     */
    default int getOrder() {
        return 0;
    }
}
